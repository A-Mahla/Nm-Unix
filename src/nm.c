# include "nm.h"


void	nm(char *filename)
{
	open_file(filename);
}


int	main(int ac, char **av)
{
	if (ac < 2) {
		nm("a.out");
	}
	for (int i = 1; i < ac; i++)
		nm(av[i]);
	return 0;
}
